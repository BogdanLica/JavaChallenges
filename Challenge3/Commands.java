package com.company;
public class Commands {


    public int clear(int operand)
    {
        operand = 0;

        return operand;
        // add both the value and the name into a 2-d list


    }
    public int incr(int operand)
    {
        operand=1;
        return operand;


    }
    public int num(int input)
    {
        return input;

    }
    public int decr(int operand)
    {
        operand=-1;
        return operand;

    }


}
